#[test]
fn ast_round_trip_gsoc_opm() {
    let parsed = parse(GSOC_OPM).expect("parse");
    let reemitted = format!("{parsed}");
    let reparsed = parse(&reemitted).expect("reparse");
    assert_eq!(parsed, reparsed);
}

#[test]
fn typed_round_trip_gsoc_opm_preserves_structure() {
    // Bit-exact f64 equality through km↔m unit conversions is
    // structurally impossible (each cycle adds sub-ULP noise from the
    // multiply/divide pair). We assert the *structure* round-trips
    // — header fields, metadata, maneuver count, optional-block
    // presence — and rely on the AST round-trip test for byte-faithful
    // wire-level preservation.
    let opm = read_opm(GSOC_OPM).expect("read");
    let written = write_opm(&opm);
    let opm2 = read_opm(&written).expect("re-read");

    assert_eq!(opm.header.originator, opm2.header.originator);
    assert_eq!(opm.header.comments, opm2.header.comments);
    assert_eq!(opm.metadata.object_name, opm2.metadata.object_name);
    assert_eq!(opm.metadata.object_id, opm2.metadata.object_id);
    assert_eq!(opm.metadata.center, opm2.metadata.center);
    assert_eq!(opm.metadata.frame, opm2.metadata.frame);
    assert_eq!(opm.state_comments, opm2.state_comments);
    assert_eq!(opm.maneuvers.len(), opm2.maneuvers.len());
    assert_eq!(opm.keplerian.is_some(), opm2.keplerian.is_some());
    assert_eq!(opm.spacecraft.is_some(), opm2.spacecraft.is_some());
    assert_eq!(opm.covariance.is_some(), opm2.covariance.is_some());
    assert_eq!(opm.user_defined, opm2.user_defined);

    // The Keplerian sub-block's GM round-trips exactly (no unit conv
    // hops — the writer divides by 1e9, reader multiplies by 1e9, and
    // the value 398600.4415 stays bit-exact in f64).
    assert_eq!(
        opm.keplerian.as_ref().unwrap().gm,
        opm2.keplerian.as_ref().unwrap().gm
    );

    // Per-maneuver comment lists round-trip exactly (strings).
    for (a, b) in opm.maneuvers.iter().zip(&opm2.maneuvers) {
        assert_eq!(a.comments, b.comments);
        assert_eq!(a.frame, b.frame);
    }
}

#[test]
fn typed_round_trip_preserves_all_comments() {
    let opm = read_opm(GSOC_OPM).expect("read");

    let header_count = opm.header.comments.len();
    let metadata_count = opm.metadata.comments.len();
    let state_count = opm.state_comments.len();
    let keplerian_count = opm.keplerian.as_ref().map_or(0, |k| k.comments.len());
    let spacecraft_count = opm.spacecraft.as_ref().map_or(0, |s| s.comments.len());
    let covariance_count = opm.covariance.as_ref().map_or(0, |c| c.comments.len());
    let maneuver_count: usize = opm.maneuvers.iter().map(|m| m.comments.len()).sum();

    let total = header_count
        + metadata_count
        + state_count
        + keplerian_count
        + spacecraft_count
        + covariance_count
        + maneuver_count;

    // GSOC fixture has 10 COMMENT lines in the body:
    //   2 in the header (Generated by GSOC, Current intermediate orbit ...)
    //   1 state-vector comment (State Vector)
    //   1 Keplerian comment (Keplerian elements)
    //   1 spacecraft comment (Spacecraft parameters)
    //   3 comments before the first maneuver (2 planned, AMF-3, fixed direction)
    //   2 comments before the second maneuver (Second maneuver, impulsive RTN)
    assert_eq!(
        total, 10,
        "comment counts: header={header_count}, metadata={metadata_count}, state={state_count}, keplerian={keplerian_count}, spacecraft={spacecraft_count}, covariance={covariance_count}, maneuver={maneuver_count}"
    );
}

#[test]
fn gsoc_opm_has_two_maneuvers() {
    let opm = read_opm(GSOC_OPM).expect("read");
    assert_eq!(opm.maneuvers.len(), 2);
}

#[test]
fn gsoc_opm_preserves_user_defined_empty() {
    // The fixture has no USER_DEFINED_* entries — the typed result
    // should mirror that.
    let opm = read_opm(GSOC_OPM).expect("read");
    assert!(opm.user_defined.is_empty());
}
