//! End-to-end round-trip tests for the KVN OPM reader/writer against a
//! realistic operator-formatted fixture (a GSOC AMF-3 OPM with
//! Keplerian elements, spacecraft parameters, and two maneuvers).
//!
//! These exercise the public API: [`lox_odm::kvn::parse`],
//! [`lox_odm::kvn::read_opm`], [`lox_odm::kvn::write_opm`], plus
//! [`std::fmt::Display`] on [`lox_odm::kvn::KvnDocument`].

