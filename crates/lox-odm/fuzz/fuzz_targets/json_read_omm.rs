//! Fuzz the JSON OMM reader: no-panic.

#![no_main]

use libfuzzer_sys::fuzz_target;

fuzz_target!(|data: &[u8]| {
    if let Ok(s) = std::str::from_utf8(data) {
        let _ = lox_odm::json::read_omm(s);
    }
});
